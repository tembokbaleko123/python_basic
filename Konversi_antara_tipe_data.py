print(float(5)) #int ke float

#Konversi Float ke Integer
print(int(5.6))
print(int(-5.6)) 

#Konversi dari-dan-ke String
print(int("25"))
print(str(25))
print(float("25"))
print(str(25.6))

#Konversi Kumpulan Data
print(set([1,2,3])) #dari tupple set
print(tuple({5,6,7})) #dari set ke tupple
print(list('hello')) #dari String ke List

#Konversi List ke Dictionary
print(dict([[1,2],[3,4]]))

#Konversi list dari beberapa tuple yang isinya pasangan nilai menjadi dictionary.
print(dict([(3,26),(4,44)]))